/**
 * Game Item Types
 *
 * Типы для игровых предметов
 */

// ============================================================================
// Item Type Enum
// ============================================================================

/**
 * Типы предметов
 */
export enum ItemType {
  POTION = "POTION",
  WEAPON = "WEAPON",
  GOLD = "GOLD",
}

// ============================================================================
// Item Action
// ============================================================================

/**
 * Типы действий предметов
 */
export enum ItemActionType {
  HEAL = "HEAL",
  DAMAGE = "DAMAGE",
  BUFF = "BUFF",
  INSTANT = "INSTANT",
}

/**
 * Действие, которое выполняет предмет при использовании
 */
export interface ItemAction {
  /** Тип действия */
  type: ItemActionType;
  /** Требуется ли цель для использования */
  requiresTarget: boolean;
  /** Значение эффекта (лечение, урон и т.д.) */
  value?: number;
  /** Описание действия */
  description?: string;
}

// ============================================================================
// Item
// ============================================================================

/**
 * Игровой предмет (клиентская модель)
 */
export interface Item {
  /** Уникальный идентификатор */
  id: string;
  /** Название предмета */
  name: string;
  /** Тип предмета */
  type: ItemType;
  /** Значение (лечение, урон или количество золота) */
  value: number;
  /** Описание предмета */
  description?: string;
  /** Действие при использовании */
  action?: ItemAction;
  /** Категория предмета */
  category?: string;
  /** Можно ли складывать в стак */
  isStackable?: boolean;
  /** Размер стака */
  stackSize?: number;
  /** Урон (для оружия) */
  damage?: number;
  /** Защита (для брони) */
  defense?: number;
  /** Вес предмета */
  weight?: number;
  /** Является ли предмет разумным */
  isSentient?: boolean;
  /** Символ для отображения (e.g., "†", "!", "$") */
  symbol?: string;
  /** Цвет символа (e.g., "#C0C0C0") */
  color?: string;
}

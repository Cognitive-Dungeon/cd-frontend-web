/**
 * Game World Types
 *
 * Типы для игрового мира (карта, тайлы)
 */

// ============================================================================
// Tile Types
// ============================================================================

/**
 * Тип окружения тайла
 */
export type TileEnv = "stone" | "grass" | "water" | "tree" | "floor";

/**
 * Тайл карты (клиентская модель)
 */
export interface Tile {
  /** Координата X */
  x: number;
  /** Координата Y */
  y: number;
  /** Является ли тайл стеной */
  isWall: boolean;
  /** Тип окружения */
  env: TileEnv;
  /** Виден ли тайл сейчас */
  isVisible: boolean;
  /** Был ли тайл исследован (для тумана войны) */
  isExplored: boolean;
}

// ============================================================================
// Game World
// ============================================================================

/**
 * Игровой мир (клиентская модель)
 */
export interface GameWorld {
  /** Двумерный массив тайлов карты [y][x] */
  map: Tile[][];
  /** Ширина карты в тайлах */
  width: number;
  /** Высота карты в тайлах */
  height: number;
  /** Текущий уровень подземелья */
  level: number;
  /** Глобальный тик игры */
  globalTick: number;
}
